/**
 * Represents a keyboard input handler.
 */

class Keyboard {

    /**
     * Creates an instance of Keyboard.
     */

    constructor() {

        /**
         * Indicates whether the right arrow key is pressed.
         * @type {boolean}
         */

        this.RIGHT = false;

        /**
         * Indicates whether the left arrow key is pressed.
         * @type {boolean}
         */

        this.LEFT = false;

        /**
         * Indicates whether the spacebar key is pressed.
         * @type {boolean}
         */

        this.SPACE = false;

        /**
         * Indicates whether the 'F' key is pressed.
         * @type {boolean}
         */
        
        this.F = false;
    }
}